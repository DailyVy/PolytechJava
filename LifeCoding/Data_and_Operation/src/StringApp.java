public class StringApp {
    public static void main(String[] args) {
        // "" 와 ''의 차이
        System.out.println("Hello World"); // String은 Character들이 모여있는 것
//        System.out.println('Hello World'); // ''는 Character만! Character는 한 글자를 표현하는 데이터타입
        System.out.println('H'); // Character
        System.out.println("H");
        //escape 문자
        System.out.println("Hello\n\"World\""); // \n : new line 개행!! Hello "World"
    }
}
